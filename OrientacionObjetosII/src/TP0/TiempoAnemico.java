package TP0;

public class TiempoAnemico {
	int dia;
	int mes;
	int anio;
	
public TiempoAnemico(int dia, int mes, int anio) {
	this.dia = dia;
	this.mes = mes;
	this.anio = anio;
}

public int getDia() {
	return dia;
}

public int getMes() {
	return mes;
}

public int getAnio() {
	return anio;
}
	

}
